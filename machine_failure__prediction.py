from sklearn.linear_model import LogisticRegression
from sklearn.metrics import classification_report, confusion_matrix
from sklearn.preprocessing import LabelEncoder
from sklearn.model_selection import train_test_split
import seaborn as sns
import matplotlib.pyplot as plt

# Define X and y for this classification task explicitly to avoid previous variable state issues
# X_classify should contain all numerical features, excluding the 'fail' column
X_classify = data.drop("fail", axis=1)
y_classify = data["fail"]

# Encode the target variable (y_classify)
le = LabelEncoder()
y_classify_encoded = le.fit_transform(y_classify)

# Split the data into training and testing sets using the newly defined X_classify and y_classify_encoded
X_train_classify, X_test_classify, y_train_encoded, y_test_encoded = train_test_split(
    X_classify, y_classify_encoded, test_size=0.2, stratify=y_classify_encoded, random_state=42
)

lr = LogisticRegression(max_iter=1000) # Increased max_iter for convergence
lr.fit(X_train_classify, y_train_encoded)
y_pred_lr = lr.predict(X_test_classify)

print(classification_report(y_test_encoded, y_pred_lr))
cm = confusion_matrix(y_test_encoded, y_pred_lr)
sns.heatmap(cm, annot=True)
plt.show()

